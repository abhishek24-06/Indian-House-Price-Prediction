import React from "react";

function formatINR(amount) {
  if (amount >= 10000000) {
    return `₹${(amount / 10000000).toFixed(2)} Cr`;
  } else if (amount >= 100000) {
    return `₹${(amount / 100000).toFixed(2)} L`;
  }
  return `₹${amount.toLocaleString("en-IN")}`;
}

export default function ResultCard({ price }) {
  const low = price * 0.9;
  const high = price * 1.1;

  return (
    <div className="result-card" role="region" aria-label="Prediction Result">
      {/* ── Green Header ── */}
      <div className="result-header">
        <span className="result-header-icon">🎉</span>
        <p className="result-label">Estimated Property Value</p>
        <p className="result-price">{formatINR(price)}</p>
      </div>

      {/* ── Body ── */}
      <div className="result-body">
        <p className="result-range-title">📊 Estimated Price Range (±10%)</p>

        <div className="result-range">
          <div className="result-range-item low">
            <p className="result-range-item-label">Conservative</p>
            <p className="result-range-item-value">{formatINR(low)}</p>
          </div>
          <div className="result-range-item est">
            <p className="result-range-item-label">Best Estimate</p>
            <p className="result-range-item-value">{formatINR(price)}</p>
          </div>
          <div className="result-range-item high">
            <p className="result-range-item-label">Optimistic</p>
            <p className="result-range-item-value">{formatINR(high)}</p>
          </div>
        </div>

        <div className="result-note">
          ⚠️ This estimate is generated by an ML model trained on historical data and is for informational purposes only.
          It should not be used as the sole basis for financial decisions.
        </div>
      </div>
    </div>
  );
}
